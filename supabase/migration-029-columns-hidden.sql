-- ============================================================
-- Migration 029 — Скриване на колони (без изтриване на данни)
-- ============================================================
-- Добавя `is_hidden` флаг на crm_columns. Скритите колони не се рендерират
-- в Работен лист / Клиенти, но самата колона + всички cell_values остават
-- в базата. Така data-та е достъпна от други екрани (Профили чете „Оценка
-- на клиент" по име) и admin може да върне видимостта когато реши.
--
-- Алтернативата „изтриване на колоната" е необратима — заради ON DELETE
-- CASCADE към crm_cell_values и crm_dropdown_options всичко свързано се
-- изтрива заедно с колоната.
--
-- Идемпотентно: пуска се повторно без грешка.
-- ============================================================

alter table crm_columns
  add column if not exists is_hidden boolean not null default false;

-- Бърз индекс за филтрирането в Работен лист — по подразбиране всички колони
-- са видими, така че индексът е малък.
create index if not exists idx_columns_hidden on crm_columns(is_hidden) where is_hidden = true;
